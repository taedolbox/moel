    if 'selected_dates' not in st.session_state:
        st.session_state.selected_dates = set()

    selected_dates = st.session_state.selected_dates
    current_date = current_datetime.date()

    start_date_for_calendar = (apply_date.replace(day=1) - pd.DateOffset(months=1)).replace(day=1).date()
    end_date_for_calendar = apply_date
    months_to_display = sorted(list(set((d.year, d.month) for d in pd.date_range(start=start_date_for_calendar, end=end_date_for_calendar))))

    with st.container():
        st.markdown('<div class="calendar-wrapper">', unsafe_allow_html=True)

        for year, month in months_to_display:
            # ì›” í—¤ë” (í˜„ì¬ ì›” ì´ë¦„ì€ í‘œì‹œí•˜ì§€ ì•ŠìŒ)
            
            # ìš”ì¼ í—¤ë” (ì›”, í™”, ìˆ˜, ëª©, ê¸ˆ, í† , ì¼) - st.columns ì‚¬ìš©
            cols = st.columns(7, gap="small")
            # ìš”ì¼ ì´ë¦„ ìˆœì„œ ë³€ê²½: ì›”ìš”ì¼ë¶€í„° ì‹œì‘
            day_names = ["ì›”", "í™”", "ìˆ˜", "ëª©", "ê¸ˆ", "í† ", "ì¼"]
            for i, day_name in enumerate(day_names):
                with cols[i]:
                    # ì£¼ë§(í† , ì¼)ë§Œ ë¹¨ê°„ìƒ‰ìœ¼ë¡œ í‘œì‹œ (ì›”ìš”ì¼ ì‹œì‘ì´ë¯€ë¡œ í† ìš”ì¼ì€ ì¸ë±ìŠ¤ 5, ì¼ìš”ì¼ì€ ì¸ë±ìŠ¤ 6)
                    color = "red" if i == 5 or i == 6 else "#000000" 
                    st.markdown(
                        f'<div class="day-header" style="color: {color};">{day_name}</div>',
                        unsafe_allow_html=True
                    )

            # ë‹¬ë ¥ ë‚ ì§œ ë Œë”ë§ - st.columns ì‚¬ìš©
            cal = calendar.monthcalendar(year, month)
            for week in cal:
                cols = st.columns(7, gap="small") # ê° ì£¼ë§ˆë‹¤ 7ê°œì˜ ì»¬ëŸ¼ ìƒì„±
                for i, day in enumerate(week):
                    with cols[i]:
                        if day == 0:
                            # í•´ë‹¹ ì›”ì— ì†í•˜ì§€ ì•ŠëŠ” ë‚ ì§œëŠ” ë¹ˆ ê³µê°„ìœ¼ë¡œ ì²˜ë¦¬
                            st.markdown('<div class="calendar-day-box" style="background-color: transparent; border: none;"></div>', unsafe_allow_html=True)
                            continue

                        date_obj = date(year, month, day)
                        is_selected = date_obj in selected_dates
                        is_current = date_obj == current_date
                        is_disabled = date_obj > apply_date # ì‹ ì²­ì¼ ì´í›„ì˜ ë‚ ì§œëŠ” ë¹„í™œì„±í™”

                        button_key = f"day_button_{year}_{month}_{day}"

                        if is_disabled:
                            # ë¹„í™œì„±í™”ëœ ë‚ ì§œëŠ” í´ë¦­ ë¶ˆê°€ëŠ¥í•˜ê²Œ í…ìŠ¤íŠ¸ë¡œ ë Œë”ë§
                            st.markdown(
                                f'<div class="calendar-day-box disabled-day">{day}</div>',
                                unsafe_allow_html=True
                            )
                        else:
                            # í™œì„±í™”ëœ ë‚ ì§œëŠ” í´ë¦­ ê°€ëŠ¥í•œ ë²„íŠ¼ìœ¼ë¡œ ë Œë”ë§
                            if st.button(str(day), key=button_key, use_container_width=True):
                                if date_obj in selected_dates:
                                    selected_dates.remove(date_obj)
                                else:
                                    selected_dates.add(date_obj)
                                st.session_state.selected_dates = selected_dates
                                st.rerun()

                            # ë™ì ìœ¼ë¡œ CSS í´ë˜ìŠ¤ ì¶”ê°€ (ë²„íŠ¼ì— ì§ì ‘ ì ìš©)
                            button_classes_to_add = []
                            if is_selected:
                                button_classes_to_add.append("selected-day")
                            if is_current and not is_selected:
                                button_classes_to_add.append("current-day")
                            
                            # ìƒì„±ëœ Streamlit ë²„íŠ¼ì— CSS í´ë˜ìŠ¤ë¥¼ ì ìš©í•˜ê¸° ìœ„í•´ <style> íƒœê·¸ë¥¼ ì£¼ì…í•©ë‹ˆë‹¤.
                            st.markdown(
                                f"""
                                <style>
                                    div[data-testid="column"] > button[key="{button_key}"] {{
                                        {" ".join(button_classes_to_add)}
                                    }}
                                </style>
                                """,
                                unsafe_allow_html=True
                            )

        st.markdown('</div>', unsafe_allow_html=True) # calendar-wrapper ë‹«ê¸°

    # í˜„ì¬ ì„ íƒëœ ê·¼ë¬´ì¼ì ëª©ë¡ í‘œì‹œ
    if st.session_state.selected_dates:
        st.markdown("### âœ… ì„ íƒëœ ê·¼ë¬´ì¼ì")
        st.markdown(", ".join([d.strftime("%Y-%m-%d") for d in sorted(st.session_state.selected_dates)]))

